set -x                                                                  ;
#########################################################################
test -n "${engine}"		|| exit 100                             ;
#########################################################################
sudo apt-get update                                                     ;
sudo apt-get install -y ${engine}.io                                    ;
sudo systemctl enable --now ${engine}                                   ;
#########################################################################
sleep=10                                                                ;
#########################################################################
while true                                                              ;
do                                                                      \
        systemctl status ${engine}                                      \
        |                                                               \
        grep running                                                    \
        &&                                                              \
        break                                                           ;
        sleep ${sleep}                                                  ;
done                                                                    ;
