set -x                                                                  ;
#########################################################################
test -n "${log}"                || exit 103                             ;
test -n "${token_token}"        || exit 106                             ;
#########################################################################
sleep=10                                                                ;
success='^This node joined a swarm'                             	;
#########################################################################
token_token="$(                                                         \
        echo ${token_token}                                             \
        |                                                               \
        base64 --decode                                                 \
)"                                                                      ;
#########################################################################
while true                                                              ;
do                                                                      \
        grep                                                            \
                "${success}"                                            \
                ${log}                                                  \
        &&                                                              \
        break                                                           ;
        sudo                                                            \
                ${token_token}                                          \
                                                                        ;
        sleep ${sleep}                                                  ;
done                                                                    ;
